package by.danilchik;

public class CalcLogic {

    public double plus(double x, double y) {
        return x + y;
    }

    public double minus(double x, double y) {
        return x - y;
    }

    public double share(double x, double y) {
        return x / y;
    }

    public double multiply(double x, double y) {
        return x * y;
    }
}
